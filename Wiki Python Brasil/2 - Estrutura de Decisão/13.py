dia_semana = input("Digite o dia da semana:\n1 - Domingo\n2 - Segunda-feira\n3 - Terça-feira\n4 - Quarta-feira\n5 - Quinta-feira\n6 - Sexta-feira\n7 - Sábado\n")

if dia_semana == "1":

    print("Domingo")

elif dia_semana == "2":

    print("Segunda-feira")

elif dia_semana == "3":

    print("Terça-feira")

elif dia_semana == "4":

    print("Quarta-feira")

elif dia_semana == "5":

    print("Quinta-feira")

elif dia_semana == "6":

    print("Sexta-feira")

elif dia_semana == "7":

    print("Sábado")

else:

    print("Valor inválido")