## Kilograms to many

def quilograma_tonelada (quilo: float):

    return f"{quilo:,.2f} Quilos equivalem à {quilo / 1000:,.2f} Toneladas"


def quilograma_grama (quilo: float):

    return f"{quilo:,.2f} Quilos equivalem à {quilo * 1000:,.2f} Gramas"


def quilograma_miligrama (quilo: float):

    return f"{quilo:,.2f} Quilos equivalem à {quilo * 1e+6:,.2e} Miligramas"


def quilograma_micrograma (quilo: float):

    return f"{quilo:,.2f} Quilos equivalem à {quilo * 1e+9:,.2e} Microgramas"


def quilograma_libra (quilo: float):

    return f"{quilo:,.2f} Quilos equivalem à {quilo * 2.205:,.2f} Libras"


def quilograma_onca (quilo: float):

    return f"{quilo:,.2f} Quilos equivalem à {quilo * 35.274:,.2f} Onças"


def quilograma_arroba (quilo: float):

    return f"{quilo:,.2f} Quilos equivalem à {quilo / 15:,.2f} Arrobas"